#include <iostream>
using namespace std;

int main() {
    cout << "int: " << sizeof(int) << " bytes\n";
    cout << "float: " << sizeof(float) << " bytes\n";
    cout << "double: " << sizeof(double) << " bytes\n";
    cout << "char: " << sizeof(char) << " bytes\n";
    cout << "long: " << sizeof(long) << " bytes\n";
    cout << "unsigned int: " << sizeof(unsigned int) << " bytes\n";
    return 0;
}
